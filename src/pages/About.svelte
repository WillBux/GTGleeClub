    <h1 class="title">About Us</h1>

    <p>
      The Georgia Tech Glee Club is the oldest student organization on the
      Georgia Tech campus. It is also the oldest Glee Club in the South, and one
      of the oldest such groups in the country. Founded in 1906, it has a rich
      tradition of singing that includes numerous national and international
      tours, as well as radio broadcasts and recordings. It was one of the first
      college organizations to record its fight song, making Ramblin' Wreck from
      Georgia Tech one of the most famous college fight songs in the country.
      The group has appeared twice on the Ed Sullivan show and has entertained
      audiences around the world.
    </p>
    <br />

    <p>
      Featuring a "healthily irreverent" blend of music, drama, and vocal/visual
      performance antics, the Georgia Tech Glee Club sings entirely original
      arrangements of usually unaccompanied music, utilizing vocal percussion,
      movement, and technology. It is a rare occasion for the Glee Club to have
      any accompniment.
    </p>
    <br />

    <p>
      For two years, the Glee Club served as <em>Ensemble-in-Residence</em> in
      Honolulu at the 2006 and 2007
      <em>Hawaii International Conference on Arts & Humanities</em>. In 2006,
      the Glee Club released its first studio CD recording of all original
      arrangements and compositions. In 2008, the ensemble made a successful
      solo debut appearance in New York City's Carnegie Hall and completed a
      successful ten-day concert tour of England, Scotland, and Wales, garnering
      rave reviews for their performances. In 2009, the Glee Club performed at
      the Smithsonian Institution in Washington D.C. and in the Russell Senate
      Building. The Glee Club closed 2009 by performing on
      <em>The Weather Channel</em>, <em>Adult Swim</em>, and <em>ESPN</em>. May
      2010 saw the Glee Club travel up and down California for a west coast
      concert tour. The group sang for adoring fans, alumni, and unsuspecting
      pedestrians. They also lifted the spirits of wounded soldiers in the San
      Diego Navy Medical Center.
    </p>
  </div>
