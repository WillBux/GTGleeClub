    <h1 class="title">Ancient History</h1>

    <p>
      It is unclear when a group of Tech men first harmonized and called
      themselves a glee club, but it is accepted that the singers represent the
      Institute's oldest student organization. "The Glee Club was first started
      in 1906 and was composed of some eight or 10 men who would gather every
      afternoon under the Academic Building and practice," the Technique
      reported in an October 9, 1917, article introducing new students to campus
      customs and clubs. An Atlanta Constitution article dated June 20, 1907,
      records the group's roots a bit differently. "One of the great social
      organizations that helps to make the thorny path at Tech tread easier is a
      musical club organized last February under the suggestive title of the
      'Tech Glee Club'."
    </p>
    <br />

    <p>
      The storied history of the Glee Club includes travels around the world on
      military transport planes, being surrounded by screaming girls, and even
      referring to themselves as "heckuva engineers" to appease Ed Sullivan. By
      1924, the club was so popular that it drew over 200 voices to the fall
      tryouts, according to the student newspaper. "Seventy-five survived the
      first cut. This number will later be cut down to 50 as soon as the
      management decides who are the sheep and who are the goats." In 1926, the
      club broadcast on WGST radio three times and staged shows throughout the
      state. A March 19, 1926 review of one tour stop said "Shorty Wiggins' act,
      which consists of a sort of Charleston clog, was the hit of the show, he
      being called back for encores so many times that he was completely
      exhausted and had to be helped off the stage."
    </p>
    <br />

    <p>
      Glee Club news routinely made the front page of the Technique. On October
      14, 1927, the selection of 35 singers made the front page alongside
      another headline: "Thousands greet Lindbergh on Grant Field." After World
      War II, the Glee Club enjoyed one of its most popular and wide-reaching
      eras as military transports flew the Tech men around the globe. One such
      world tour resulted in a January 1951 headline in the Atlanta
      Constitution: "The Tech Glee Club returns from 7,525-mile jaunt."
    </p>
    <br />

    <p>
      In 1953, Ed Sullivan's "Toast of the Town" attracted an estimated 30
      million television viewers. The 50 Glee Club members auditioned for 28
      seats on the train that would take them to New York. Three songs were
      planned: "Ramblin' Wreck," "There's Nothin' Like a Dame," and the Alma
      Mater. Sullivan went beyond insisting that the men sing that they were
      "heckuva engineers," according to the Technique. "The club sang 'Dames' at
      rehearsal and brought down the house, only to have Sullivan give it the
      axe." The Glee Club was invited for a return appearance in 1968.
    </p>
    <br />

    <p>
      Over their 100+ year history, the group has literally traveled the world,
      appearing in Greenland, Labrador, Korea, the South Pacific, Europe, South
      America, the Caribbean, and throughout the United States. Their history
      has always included an "out of the box" spirit, in keeping with the
      Georgia Tech motto, We don't fit the mold; we make it.
    </p>
  </div>
</section>

