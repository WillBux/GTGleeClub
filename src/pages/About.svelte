    <h1 class="title">Modern History</h1>

    <p>
      After the golden years, the 1970s saw a dropoff in interest and
      attendence, which never really picked back up. By 2003, the Club was
      nearly dead.
    </p>
    <br />

    <p>
      At the first rehearsal after the arrival of its current director Dr. Jerry
      Ulrich in 2003, the Glee Club numbered 13 singers, including at least two
      persons who could not match pitch. That fall semester featured one public
      performance on the November Choral Concert where the group was
      "sandwiched" between the Chamber Choir, the Chorale, and two student-run a
      cappella groups.
    </p>
    <br />

    <p>
      In the ensuing five semesters, the group expanded to 50 singers, and
      performed hundreds of times, including a featured performance for the
      Georgia Board of Regents at the GT President's residence, home basketball
      games (singing the National Anthem and a half-time feature), homecoming
      football games, the President's Gala at Tech Square (for the presidents of
      every public college and university in Georgia), Earthlink Live (Greek
      Sing), the Student Success Center, the GT Economic Development Institute,
      Connect With Tech, Spivey Hall (as a part of the 2004 choral concert), the
      College of Architecture Awards Day Ceremony, featured performances at
      Atlanta area churches, and community outreach performances, including
      Hands On Atlanta> at the Kings Bridge Assisted Living Facility.
    </p>
    <br />

    <p>
      Additionally, the group introduced Live Music for Dead Week performances
      in the dining halls (coordinated and sponsored by GT Dining) during the
      last week of academic classes, and Sorority Serenade, an evening of
      singing for sorority houses on campus (both invited and spontaneous). The
      group has also provided numerous entertainment programs on the campus and
      in the Atlanta community, such as the Administrative Professionals
      Breakfast, Faculty Women's Club luncheon, Alumni Association events, and
      FASET. The Glee Club hosted Ultimate A Cappella clinic in collaboration
      with the Southeast U.S. region barbershop society.
    </p>
    <br />

    <p>
      Spring 2005 featured the first full feature concert (in recent history) by
      the Men's Glee Club in the Student Center Theater. In April 2005 the Men's
      Glee Club was featured at the High Museum of Art, Hill Auditorium as a
      part of their After Hours at the High program.
    </p>
    <br />

    <p>
      In the past few years, the group has sung over 500 performances for
      thousands of people, including performances for GT Presidents Clough and
      Peterson, Mayor Shirley Franklin, Former Secretary of State Colin Powell,
      Governor Sonny Perdue, U.S. Senator Johnny Isakson, the Atlanta Braves at
      Turner Field, the Alumni Association Board, the Georgia Tech Advisory
      Board, the Georgia Tech Foundation, AirTran Airlines Atlanta managerial
      staff, AirTran Christmas party, hundreds of weary holiday travelers in
      numerous performances in the Atlanta Hartsfield-Jackson International
      Airport, and the Georgia Tech Black History Month Celebration. In January
      2006 and 2007, the Glee Club served as Ensemble-In-Residence at the 4th
      and 5th Annual Hawaii International Conference of Arts & Humanities in
      Honolulu. The conference audiences included over 1500 academicians from
      over 40 countries.
    </p>
    <br />

    <p>
      The Glee Club has opened for Rockapella, Horatio Sanz of Saturday Night
      Live fame, and shared the stage with Kenny Chesney and Joe Walsh. They
      have appeared live on ESPN, The Weather Channel, and Adult Swim on Cartoon
      Network. In April 2008, the group debuted in New York's Carnegie hall and
      the following month completed their first concert tour of the UK. In May
      2010, the Glee Club embarked on a concert tour of California.
    </p>
    <br />

    <p>It's safe to say the Glee Club is now alive and doing well!</p>
